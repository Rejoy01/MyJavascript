// always use const if the type should not be changed (Arrays and Objects)

// mixed examples

const price1 = 5
const price2 = 6
let total = price1+price2;

console.log(total);

// value of price1 and price2 can't be chnage while it is declared with const
// here value total can be changed as it is declared with let

